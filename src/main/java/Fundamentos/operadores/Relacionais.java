package Fundamentos.operadores;

public class Relacionais {
    public static void main(String[] args) {
        int n1 = 12;
        int n2 = 14;

        System.out.println(n1 > n2);
        System.out.println(n1 < n2);
        System.out.println(n1 >= n2);
        System.out.println(n1 <= n2);
        System.out.println(n1 == n2);
        System.out.println(n1 != n2);
    }
}
