public class For1 {
    public static void main(String[] args) {
        for ( int contador = 0; contador <= 20 ; contador ++ ){
            System.out.print(contador + "/");
        }
        System.out.println("");
        int num1 = 0;
        for (; num1 <= 40 ;){
            System.out.print(num1 + "/" );
            num1 +=4;
        }
    }
}
