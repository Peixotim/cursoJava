package Classe;

public class Calculadora {
    int soma(int x , int y){
        return x+y;
    }

    int subtracao(int x , int y){
        return x-y;
    }

    int multiplicacao(int x , int y){
        return x*y;
    }

    int divisao(int x , int y){
        return x/y;
    }

}
